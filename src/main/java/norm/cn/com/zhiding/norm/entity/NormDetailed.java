package cn.com.zhiding.norm.entity;

import java.math.BigDecimal;
import java.util.Date;

public class NormDetailed {
    private Long id;

    private Long moduleId;

    private BigDecimal average;

    private BigDecimal standardScore;

    private Long normId;

    private Date createDate;

    private Integer status;
    
    private Module module;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getModuleId() {
        return moduleId;
    }

    public void setModuleId(Long moduleId) {
        this.moduleId = moduleId;
    }

    public BigDecimal getAverage() {
        return average;
    }

    public void setAverage(BigDecimal average) {
        this.average = average;
    }

    public BigDecimal getStandardScore() {
        return standardScore;
    }

    public void setStandardScore(BigDecimal standardScore) {
        this.standardScore = standardScore;
    }

    public Long getNormId() {
        return normId;
    }

    public void setNormId(Long normId) {
        this.normId = normId;
    }

    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

	public Module getModule() {
		return module;
	}

	public void setModule(Module module) {
		this.module = module;
	}
    
    
}